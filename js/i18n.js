function detectLang() {
    const saved = localStorage.getItem('gb_lang');
    if (saved) return saved;
    const nav = (navigator.language || navigator.userLanguage || 'pt').toLowerCase();
    return nav.startsWith('pt') ? 'pt' : 'en';
}

function applyLang(lang) {
    const t = translations[lang];
    if (!t) return;

    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';
    localStorage.setItem('gb_lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!t[key]) return;

        const attr = el.getAttribute('data-i18n-attr');
        if (attr) {
            el.setAttribute(attr, t[key]);
        } else {
            el.innerHTML = t[key];
        }
    });

    // Update lang toggle button label
    const btn = document.getElementById('langToggle');
    if (btn) btn.innerHTML = t['nav_lang_btn'];
}

function toggleLang() {
    const current = localStorage.getItem('gb_lang') || detectLang();
    applyLang(current === 'pt' ? 'en' : 'pt');
}

window.__i18n = { detectLang, applyLang, toggleLang, translations };
